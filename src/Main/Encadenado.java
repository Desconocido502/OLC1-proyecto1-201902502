public class Encadenado {

    private String id;
    private String encadenado;

    public Encadenado(String id, String encadenado) {
        this.id = id;
        this.encadenado = encadenado;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEncadenado() {
        return encadenado;
    }

    public void setEncadenado(String encadenado) {
        this.encadenado = encadenado;
    }

}
